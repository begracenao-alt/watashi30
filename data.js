/*
 * Be Grace メッセージ
 * 診断タイプの messages 束から、毎回ランダムで1つ返す（来るたび違う言葉に出会える）。
 */
function beGraceMessage(type) {
  const list = (type && type.messages) || [];
  if (!list.length) return '';
  return list[Math.floor(Math.random() * list.length)];
}

function digitsSum(s) {
  return String(s)
    .replace(/\D/g, '')
    .split('')
    .reduce((a, c) => a + Number(c), 0);
}

function digitalRoot(n) {
  while (n > 9) n = digitsSum(n);
  return n;
}

/*
 * birthdate: 'YYYY-MM-DD' 等（記号は無視して数字だけ使う）
 * 生年月日"だけ"から「生まれ持ったリズム」を返す（固定）。未入力なら null。
 */
function bornRhythm(birthdate) {
  if (!birthdate) return null;
  const root = digitalRoot(digitsSum(birthdate)) || 9;
  return RHYTHMS[root - 1];
}

/*
 * 診断ロジック
 * selectedIds: 選ばれた FEELINGS の id（3つ想定）
 * 戻り値: { type, mood, scores }
 *   type : 3つのタグで一番多い7タイプ（同点はやさしい優先順）
 *   mood : 'beautiful'（進みやすい日）/ 'suffering'（整えながら進む日）
 *          ＝3つのうち多い方。同数や suffering 多数なら suffering（"整える"を大切にする）
 */
function diagnose(selectedIds) {
  const picks = selectedIds.map((id) => FEELINGS.find((x) => x.id === id)).filter(Boolean);

  // まず判定（mood）：beautiful（進む）と suffering（整える）の多い方
  let beautiful = 0;
  let suffering = 0;
  picks.forEach((f) => (f.mood === 'beautiful' ? (beautiful += 1) : (suffering += 1)));
  const mood = beautiful > suffering ? 'beautiful' : 'suffering';

  // タイプは「判定(mood)に合う選択肢」の中だけで多数決する。
  // → 「疲れてる・モヤモヤ（整える）なのにチャレンジ日和（進む）」のような
  //    判定とタイプの食い違いを防ぐ。同数はやさしい優先順（priority 昇順）。
  const pool = picks.filter((f) => f.mood === mood);
  const scores = {};
  Object.keys(TYPES).forEach((k) => (scores[k] = 0));
  pool.forEach((f) => (scores[f.type] += 1));

  let best = null;
  Object.values(TYPES).forEach((type) => {
    const s = scores[type.id];
    if (
      best === null ||
      s > scores[best.id] ||
      (s === scores[best.id] && type.priority < best.priority)
    ) {
      best = type;
    }
  });

  return { type: best, mood, scores };
}
